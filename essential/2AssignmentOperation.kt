fun main(){
    var MyNum=5   // assignment operators (+=,-=,*=,/=)
    MyNum+=3
    MyNum*=4
    MyNum-=6
    println("my new Num :$MyNum")

    //increamnt decrement opration (++,--)
    MyNum--
    println("my num : $MyNum")
    println("my num1: ${MyNum++}")
    println("my num2: ${++MyNum}")
    println("my num3 : ${MyNum--}")
}